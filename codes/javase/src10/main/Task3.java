public class Task3 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;

        System.out.println("a && b (AND): " + (a && b));
        System.out.println("a || b (OR): " + (a || b));
        System.out.println("!a (NOT a): " + (!a));
        System.out.println("!b (NOT b): " + (!b));
    }
}
